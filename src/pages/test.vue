<script setup lang="ts">
    import { definePageMeta } from '#imports'
    import StealthLogo from '@/assets/logos/stealth_logo.svg?component'
    import StealthLogoBlack from '@/assets/logos/stealth_logo_black.svg?component'
    import IconDeposit from '@/assets/logos/stealth_logo.svg?component'

    import Swiper from 'swiper'
    import 'swiper/css'
    import 'swiper/css/pagination'

    const ratingResult = ref<HTMLDivElement>()
    const stars = ref<HTMLElement[]>([])

    const slider = () => {
        new Swiper ('.swiper', {
            slidesPerView:1,
            pagination: {
                el: '.swiper-pagination',
                clickable: true,
            },
            spaceBetween: 10,
            loop: true,
            autoplay: {
                delay: 3000
            },
        })
    }

    const rating = () => {
        let selectedRating = 0;

        stars.value.forEach((star, index) => {
            star.addEventListener('mouseenter', () => {
                for (let i = 0; i <= index; i++) {
                    stars.value[i].style.backgroundColor = '#00b57a';
                }
            });

            star.addEventListener('mouseleave', () => {
                stars.value.forEach((star, i) => {
                    if (i >= selectedRating) {
                        star.style.backgroundColor = '#ccc';
                    }
                });
            });

            star.addEventListener('click', () => {
                selectedRating = index + 1;
                displayRating(selectedRating);
            });
        });

        const displayRating = (rating: any) => {
            if (!ratingResult.value) return undefined

            ratingResult.value.innerHTML = `${rating} out of 5`;
        }
    }

    const cardsModal = () => {
        const cards = document.querySelectorAll('.card');

        cards.forEach(card => {
            const modal = card.querySelector('.modal__card') as HTMLElement;
            const video = modal.querySelector('video') as HTMLVideoElement;
            const closeBtn = modal.querySelector('.close') as HTMLElement;
            const videoSrc = card.getAttribute('data-video-src') as string;

            card.addEventListener('click', () => {
                video.src = videoSrc;
                modal.style.display = 'block';
            });

            closeBtn.addEventListener('click', () => {
                modal.style.display = 'none';
                video.pause();
                video.src = '';
            });

            window.addEventListener('click', (event) => {
                if (event.target === modal) {
                    modal.style.display = 'none';
                    video.pause();
                    video.src = '';
                }
            });
        });
    };


    onMounted(() => {
        slider()
        rating()
        cardsModal()
    })

    definePageMeta({
        layout: 'test'
    })
</script>

